    private void InitCombobox(){
        for (MeasurementUnity item : MeasurementUnity.values()) {
            txtUnity.addItem(item.toString());
        }
    }
    
    private void cleanFields(){
        txtId.setText("");
        txtName.setText("");
        txtDesc.setText("");
        txtQuantity.setText("");
        txtUnity.setSelectedIndex(0);
        txtDateExpiration.setText("");
    }
